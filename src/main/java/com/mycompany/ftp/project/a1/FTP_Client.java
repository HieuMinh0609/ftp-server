     private void connect(String host, int port, String username, String password) throws Exception {
        try {
            ftpClient.connect(host, port);
             int  replyCode = ftpClient.getReplyCode();
            if (!FTPReply.isPositiveCompletion(replyCode)) {
                throw new Exception("FTP serve refused connection.");
            }
 
            boolean logged = ftpClient.login(username, password);
            if (!logged) {
                // failed to login
                ftpClient.disconnect();
                throw new Exception("Could not login to the server.");
            }
            
            isLogin = true;
            ftpClient.enterLocalPassiveMode();
            showListFile();
        } catch (Exception ex) {
            throw new Exception("I/O error: "+ ex.getMessage());
        }
    }
    
    private void showListFile() throws IOException {
        if(!isLogin) return;
        FTPFile[] files1 = ftpClient.listFiles("/");
        printFileDetails(files1);
    }
    
     private void printFileDetails(FTPFile[] files) {
        DateFormat dateFormater = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        DefaultTableModel model = new DefaultTableModel();
        
        final String[] columns = new String[]{  "Tên File", "Kiểu file", "Danh mục", "Ngày Upload", "Kích thước", "Người tạo"};
        model.setColumnIdentifiers(columns);

        for (FTPFile file : files) {
            String nameFile = file.getName();
            if (file.isDirectory()) nameFile = "[" + nameFile + "]";
            double size = Math.ceil((double) file.getSize() / 1000);
            String timeCreate = dateFormater.format(file.getTimestamp().getTime());
            String category = file.getGroup();
            String userCreate = file.getUser();
            String typeFile = file.isDirectory() ? "Directory" : "File";
            
            model.addRow(new Object[]{nameFile, typeFile, category, timeCreate, size + " (kb)", userCreate});     
        }
        this.jTable1.setModel(model);
    }

